function darkmode(){
    document.getElementById("q").style.backgroundColor="black";
    document.getElementById("q").style.color="white";
    document.getElementById("title").style.color="lightblue";
    document.getElementById("pic1").style.borderColor="rgb(164, 239, 245)";
}

function lightmode(){
    document.getElementById("q").style.backgroundColor="white";
    document.getElementById("q").style.color="black";
    document.getElementById("title").style.color="navy";
    document.getElementById("pic1").style.borderColor="blue";
}